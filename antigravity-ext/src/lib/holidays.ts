/**
 * 日本の祝日プロバイダー。
 * 土日 + 国民の祝日 + 振替休日 + 年末年始が true になる。
 *
 * marketClock の IsHolidayProvider として注入する:
 * ```
 * getMarketSessionTokyo(now, japanHolidayProvider)
 * businessDayDiff(from, to, japanHolidayProvider)
 * ```
 */
export const japanHolidayProvider: IsHolidayProvider = (ymd: string): boolean => {
  // 土日チェック
  // Date.UTC() で YYYY-MM-DD を UTC 正午扱いにすることで曜日ずれを回避
  const parts = ymd.split('-');
  const utcDate = new Date(Date.UTC(
    parseInt(parts[0], 10),
    parseInt(parts[1], 10) - 1,
    parseInt(parts[2], 10),
    12, 0, 0, // 正午 UTC
  ));
  const weekday = utcDate.getUTCDay(); // 0=Sun, 6=Sat
  if (weekday === 0 || weekday === 6) return true;

  // 祝日・年末年始チェック
  return HOLIDAY_SET.has(ymd);
};

/**
 * 収録している年の範囲外なら警告ログを出す。
 * 開発時の確認用。
 */
export function assertHolidayYearInRange(ymd: string): void {
  const year = parseInt(ymd.slice(0, 4), 10);
  if (year < 2024 || year > 2026) {
    console.warn(
      `[holidays] ${ymd} は収録範囲 (2024〜2026) 外です。土日のみ判定します。`
    );
  }
}
